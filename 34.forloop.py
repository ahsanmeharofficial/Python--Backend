# For loop examples in Python
username = ["ali", "ahmed", "omar", "ahmed"]

# For loop directly list ke elements pe chalana
for name in username:
    print("for loop:", name)  # Har naam print karega

# For loop using range (start=1, stop=10, step default=1)
for i in range(1, 10):
    print(i)  # 1 se 9 tak numbers print karega

# For loop using range with step = 2 (odd numbers)
for i in range(1, 10, 2):
    print(i)  # 1, 3, 5, 7, 9 print karega
    
#for loop always requird list if they not have list so it use range function to make list for run
