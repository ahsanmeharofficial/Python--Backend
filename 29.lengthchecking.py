studentNames = ["Ahsan", "Bilal", "Usman", "Sana", "Hina"]

# len() ka use:
# Ye list ke total elements count karta hai
totalStudents = len(studentNames)

print("Total students:", totalStudents)  # Output: 5
# Note: Agar list empty hai, to ye 0 return karega
# Agar kisi specific element ki length chahiye, to wo bhi len() se kar sakte hain