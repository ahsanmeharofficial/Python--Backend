  # examples of storing text in Python
  
# String (text) data type
text1 = "Yeh aik string hai"  # string ko "" ya '' me likhte hain

# Integer (poori number) data type
num1 = 123  # yeh aik integer hai, bina decimal ke

# Float (decimal number) data type
num2 = 45.67  # yeh aik float hai, decimal ke sath

# Boolean (true/false) data type
flag = True  # yeh aik boolean hai, sirf True ya False hota hai

# List (multiple values ek sath)
my_list = ["apple", "banana", "cherry"]  # list me multiple values hoti hain

# Tuple (fixed values, change nahi hoti)
my_tuple = ("red", "green", "blue")  # tuple bhi multiple values store karta hai

# Dictionary (key-value pair)
my_dict = {"name": "Ali", "age": 22}  # dictionary me key aur value hoti hai

# Set (unique values)
my_set = {"a", "b", "c"}  # set me har value unique hoti hai

# Print kar ke check kar sakte hain
print(text1)      # string
print(num1)       # integer
print(num2)       # float
print(flag)       # boolean
print(my_list)    # list
print(my_tuple)   # tuple
print(my_dict)    # dictionary
print(my_set)     #

  
  
