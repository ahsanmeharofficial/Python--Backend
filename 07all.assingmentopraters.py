# Assignment Operators in Python - Beginner Level
# Assignment operators ka use variables ko value assign karne aur update karne ke liye hota hai.
# Neeche table ki tarah sabhi assignment operators ke examples diye gaye hain:

# Variable
num = 10

# =   (Simple assignment)
num = 10
print("num = 10   =>", num)  # 10

# +=  (Addition assignment)
num += 5
print("num += 5   =>", num)  # 15

# -=  (Subtraction assignment)
num -= 3
print("num -= 3   =>", num)  # 12

# *=  (Multiplication assignment)
num *= 2
print("num *= 2   =>", num)  # 24

# /=  (Division assignment)
num /= 4
print("num /= 4   =>", num)  # 6.0

# //= (Floor division assignment)
num //= 2
print("num //= 2  =>", num)  # 3.0

# %=  (Modulus assignment)
num %= 2
print("num %= 2   =>", num)  # 1.0

# **= (Exponent assignment)
num **= 3
print("num **= 3  =>", num)  # 1.0

# &=  (Bitwise AND assignment)
num &= 2
print("num &= 2   =>", num)  # 0

# |=  (Bitwise OR assignment)
num |= 5
print("num |= 5   =>", num)  # 5

# ^=  (Bitwise XOR assignment)
num ^= 1
print("num ^= 1   =>", num)  # 4

# >>= (Bitwise right shift assignment)
num >>= 1
print("num >>= 1  =>", num)  # 2

# <<= (Bitwise left shift assignment)
num <<= 2
print("num <<= 2  =>", num)  # 



