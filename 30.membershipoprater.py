studentNames = ["Ahsan", "Bilal", "Usman", "Sana", "Hina"]

# 'in' ka use:
# Ye check karega ke "Ahsan" list me hai ya nahi
if "Ahsan" in studentNames:
    print("Ahsan class me hai.")

# 'not in' ka use:
# Ye check karega ke "Shahzad" list me nahi hai
if "Shahzad" not in studentNames:
    print("Shahzad class me nahi hai.")
