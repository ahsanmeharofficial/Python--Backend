# Simple if condition example
# Example 1: Simple if
number = 5
if number > 0:  # agar number 0 se bada hai
    print("Number is positive")  # message print hoga

# Example 2: if-else
age = 16
if age >= 18:  # agar age 18 ya zyada hai
    print("You can vote")
else:  # warna
    print("You cannot vote")

# Example 3: if-elif-else
temperature = 25
if temperature > 30:  # agar temp 30 se zyada hai
    print("It's hot")
elif temperature > 20:  # warna agar temp 20 se zyada hai
    print("It's warm")
else:  # warna
    print("It's cold")
